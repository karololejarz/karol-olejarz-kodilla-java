package com.kodilla.good.patterns.challenges.order;

import java.time.LocalDateTime;

public class Application {
    public static void main(String[] args) {

        InformationServiceImpl infoService = new InformationServiceImpl();
        OrderServiceImpl orderService = new OrderServiceImpl();
        OrderRepository orderRepo = new OrderRepositoryImpl();

        User user1 = new User("user@user.com");
        Product product1 = new Product(1,"product1", 1.00);
        OrderRequest order = new OrderRequest(1, user1, product1, 5, LocalDateTime.now());
        ProductOrderService productOrderService = new ProductOrderService(infoService, orderService, orderRepo);
        productOrderService.process(order);

    }
}
